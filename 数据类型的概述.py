
"""数据类型的概述"""

"""
一、什么是数据类型
    数据类型是对数据的分类，例如：整数类型、浮点类型、字符串类型，等等。
    任何数据都有明确的数据类型，例如：18属于整数类型，5.6属于浮点类型，'Hello'属于字符串类型。
    
二、获取数据的数据类型
    调用内置函数type可以获取数据的数据类型。
"""
print(type(18))          # <class 'int'>
print(type(5.6))         # <class 'float'>
print(type('Hello'))     # <class 'str'>

