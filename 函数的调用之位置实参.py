
"""函数的调用之位置实参"""

"""
    调用函数时，可以根据每个形参在所有形参中的位置传递对应位置的实参，从而用每个实参初始化
对应位置的形参，这样的实参称为位置实参。
    上节课在讲"调用函数"时我们举的例子都是位置实参。
"""
def f(a, b, c):
    print('a =', a, 'b =', b, 'c =', c)

f(2, 5, 8)  # a = 2 b = 5 c = 8

"""
    《图解Python》
"""

f(5, 8, 2)  # a = 5 b = 8 c = 2

"""
    《图解Python》
"""

f(8, 5, 2)  # a = 8 b = 5 c = 2

