
"""取余运算符"""

"""
一、什么是取余运算符
    取余运算符用百分号%表示，
    a % b = a - b * 倍数 = a - b * (a // b) = 余数，
    也就是说，a对b取余就是计算a最多可以容纳多少个b，多出来的那部分就是余数。
"""
print(9 % 4)        # 1
print(1.25 % 0.5)   # 0.25

"""
# 9 = (-4) * (-3) + (-3)，因为-3处于(-4, 0)
print(9 // -4)      # -3

# -9 = 4 * (-3) + 3，因为2处于(0, 4)
print(-9 // 4)      # -3

# -9 = (-4) * 2 + (-1)，因为-1处于(-4, 0)
print(-9 // -4)     # 2
"""
print(9 % -4)        # -3
print(-9 % 4)        # 3
print(-9 % -4)       # -1
